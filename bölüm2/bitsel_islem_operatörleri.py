#burada sayılar 0 1 bitleri şeklinde üst üste yazılır işlemlere tabi tutulur
a=30
b=45
print(a&b) #ve
print(a|b) #veya
print(a^b) #xor işlemidir. İki bit birbirinden farklı ise 1, aynı ise 0 sonucunu verir
print(~a) #tümleyen işlemi yapar
print(a<<2) # a sayısındaki bitleri 2 bit sola kaydır anlamına gelir
print(a>>2) # a sayısındaki bitleri 2 bit sağa kaydır anlamına gelir
